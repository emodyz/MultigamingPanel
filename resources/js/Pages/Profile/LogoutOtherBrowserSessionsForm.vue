<template>
  <jet-action-section>
    <template #title>
      Browser Sessions
    </template>

    <template #description>
      Manage and logout your active sessions on other browsers and devices.
    </template>

    <template #content>
      <div class="max-w-xl text-sm text-zinc-600 dark:text-zinc-300">
        If necessary, you may logout of all of your other browser sessions across all of your devices. Some of your
        recent sessions are listed below; however, this list may not be exhaustive. If you feel your account has been
        compromised, you should also update your password.
      </div>

      <!-- Other Browser Sessions -->
      <div
          v-if="sessions.length > 0"
          class="mt-5 space-y-6"
      >
        <div
            v-for="(session, i) in sessions"
            :key="i"
            class="flex items-center"
        >
          <div>
            <svg
                v-if="session.agent.is_desktop"
                fill="none"
                stroke-linecap="round"
                stroke-linejoin="round"
                stroke-width="2"
                viewBox="0 0 24 24"
                stroke="currentColor"
                class="w-8 h-8 text-zinc-500 dark:text-zinc-400"
            >
              <path
                  d="M9.75 17L9 20l-1 1h8l-1-1-.75-3M3 13h18M5 17h14a2 2 0 002-2V5a2 2 0 00-2-2H5a2 2 0 00-2 2v10a2 2 0 002 2z"/>
            </svg>

            <svg
                v-else
                xmlns="http://www.w3.org/2000/svg"
                viewBox="0 0 24 24"
                stroke-width="2"
                stroke="currentColor"
                fill="none"
                stroke-linecap="round"
                stroke-linejoin="round"
                class="w-8 h-8 text-zinc-500 dark:text-zinc-400"
            >
              <path
                  d="M0 0h24v24H0z"
                  stroke="none"
              />
              <rect
                  x="7"
                  y="4"
                  width="10"
                  height="16"
                  rx="1"
              />
              <path d="M11 5h2M12 17v.01"/>
            </svg>
          </div>

          <div class="ml-3">
            <div class="text-sm text-zinc-600 dark:text-zinc-400">
              {{ session.agent.platform }} - {{ session.agent.browser }}
            </div>

            <div>
              <div class="text-xs text-zinc-500 dark:text-zinc-300">
                {{ session.ip_address }},

                <span
                    v-if="session.is_current_device"
                    class="text-green-500 dark:text-emerald-400 font-semibold"
                >This device</span>
                <span v-else>Last active {{ session.last_active }}</span>
              </div>
            </div>
          </div>
        </div>
      </div>

      <div class="flex items-center mt-5">
        <jet-button @click.native="confirmLogout">
          Logout Other Browser Sessions
        </jet-button>

        <jet-action-message
            :on="form.recentlySuccessful"
            class="ml-3"
        >
          Done.
        </jet-action-message>
      </div>

      <!-- Logout Other Devices Confirmation Modal -->
      <jet-dialog-modal
          :show="confirmingLogout"
          @close="confirmingLogout = false"
      >
        <template #title>
          Logout Other Browser Sessions
        </template>

        <template #content>
          Please enter your password to confirm you would like to logout of your other browser sessions across all of
          your devices.

          <div class="mt-4">
            <jet-input
                ref="password"
                v-model="form.password"
                type="password"
                class="mt-1 block w-3/4"
                placeholder="Password"
                @keyup.enter.native="logoutOtherBrowserSessions"
            />

            <jet-input-error
                v-if="form.errors.password"
                :message="form.errors.password"
                class="mt-2"
            />
          </div>
        </template>

        <template #footer>
          <jet-secondary-button @click.native="confirmingLogout = false">
            Nevermind
          </jet-secondary-button>

          <jet-button
              class="ml-2"
              :class="{ 'opacity-25': form.processing }"
              :disabled="form.processing"
              @click.native="logoutOtherBrowserSessions"
          >
            Logout Other Browser Sessions
          </jet-button>
        </template>
      </jet-dialog-modal>
    </template>
  </jet-action-section>
</template>

<script lang="ts">
import JetActionMessage from '@/Jetstream/ActionMessage.vue'
import JetActionSection from '@/Jetstream/ActionSection.vue'
import JetButton from '@/Jetstream/Button.vue'
import JetDialogModal from '@/Jetstream/DialogModal.vue'
import JetInput from '@/Jetstream/Input.vue'
import JetInputError from '@/Jetstream/InputError.vue'
import JetSecondaryButton from '@/Jetstream/SecondaryButton.vue'

import {
  Mixins, Component, Prop, Ref,
} from 'vue-property-decorator'
import Route from '@/Mixins/Route'

@Component({
  components: {
    JetActionMessage,
    JetActionSection,
    JetButton,
    JetDialogModal,
    JetInput,
    JetInputError,
    JetSecondaryButton,
  },
})
export default class LogoutOtherBrowserSessionsForm extends Mixins(Route) {
  @Ref('password') readonly password!: any

  @Prop() readonly sessions!: any

  confirmingLogout: boolean = false

  form = this.$inertia.form({
    password: '',
  })

  confirmLogout() {
    this.confirmingLogout = true

    setTimeout(() => {
      this.password.focus()
    }, 250)
  }

  logoutOtherBrowserSessions() {
    this.form.delete(this.route('other-browser-sessions.destroy'), {
      preserveScroll: true,
      preserveState: true,
      errorBag: 'logoutOtherBrowserSessions',
      onSuccess: () => {
        this.form.reset()
        this.confirmingLogout = false
      },
    })
  }
}
</script>
